<template>
    <div>
        <b-modal
            size="lg"
            title="Exporter des données dans un fichier"
            ok-title="Créer pdf"
            :ok-only="true"
            ref="askExportModal"
            @ok="getPdf"
            @hidden="resetModal"
        >
            <b-tabs v-model="tabIndex">
                <b-tab
                    v-if="$store.state.settings.enable_disciplinary_council"
                    title="Conseil de discipline"
                    active
                >
                    <b-row class="mt-4">
                        <b-col>
                            <b-form-row>
                                <b-form-group label="À partir du">
                                    <input
                                        type="date"
                                        v-model="date_council_from"
                                        :max="date_council_to"
                                    >
                                </b-form-group>
                            </b-form-row>
                        </b-col>
                        <b-col>
                            <b-form-row>
                                <b-form-group label="Jusqu'au">
                                    <input
                                        type="date"
                                        v-model="date_council_to"
                                        :min="date_council_from"
                                    >
                                </b-form-group>
                            </b-form-row>
                        </b-col>
                    </b-row>
                </b-tab>
                <b-tab title="Retenues">
                    <b-row class="mt-4">
                        <b-col>
                            <b-form-row>
                                <b-form-group label="À partir du">
                                    <input
                                        type="date"
                                        v-model="date_retenues_from"
                                        :max="date_retenues_to"
                                    >
                                </b-form-group>
                            </b-form-row>
                        </b-col>
                        <b-col>
                            <b-form-row>
                                <b-form-group label="Jusqu'au">
                                    <input
                                        type="date"
                                        v-model="date_retenues_to"
                                        :min="date_retenues_from"
                                    >
                                </b-form-group>
                            </b-form-row>
                        </b-col>
                    </b-row>
                    <b-row>
                        <b-col>
                            <b-form-group>
                                <b-checkbox
                                    v-model="sortByClasse"
                                >
                                    Trier par classe
                                </b-checkbox>
                            </b-form-group>
                        </b-col>
                    </b-row>
                    <b-row>
                        <b-col>
                            <b-form-group>
                                <b-checkbox
                                    v-model="sortBySanction"
                                >
                                    Trier par sanction
                                </b-checkbox>
                            </b-form-group>
                        </b-col>
                    </b-row>
                    <b-row>
                        <b-col>
                            <b-form-group>
                                <b-checkbox
                                    v-model="sanction_not_done"
                                >
                                    Sanctions non faites uniquement
                                </b-checkbox>
                            </b-form-group>
                        </b-col>
                    </b-row>
                </b-tab>
            </b-tabs>
        </b-modal>
    </div>
</template>

<script>
import Moment from "moment";
Moment.locale("fr");

export default {
    props: {
        "entriesCount": {
            type: Number,
            default: 0
        },
    },
    data: function () {
        return {
            tabIndex: 0,
            date_council_from: null,
            date_council_to: null,
            date_retenues_from: null,
            date_retenues_to: null,
            sanction_not_done: false,
            sortByClasse: this.$store.state.settings.export_retenues_by_classe_default,
            sortBySanction: this.$store.state.settings.export_retenues_by_sanction_default,
        };
    },
    watch: {
        date_council_from: function (date) {
            if (this.date_council_to === null) this.date_council_to = date;
        },
        date_retenues_from: function (date) {
            if (this.date_retenues_to === null) this.date_retenues_to = date;
        },
    },
    methods: {
        show: function () {
            this.$refs.askExportModal.show();
        },
        hide: function () {
            this.$refs.askExportModal.hide();
        },
        resetModal: function () {
            this.tabIndex = 0;
            this.date_council_from = null;
            this.date_council_to = null;
            this.date_retenues_from = null;
            this.date_retenues_to = null;
        },
        getPdf: function (evt) {
            evt.preventDefault();

            let path = "/dossier_eleve/get_pdf_";
            if (this.tabIndex == 0 && this.$store.state.settings.enable_disciplinary_council) {
                path += "council/?datetime_conseil__gt=" + this.date_council_from;
                path += " 00:00&datetime_conseil__lt=" + this.date_council_to + " 23:59";
            } else {
                path += "retenues/?activate_all_retenues=true";
                path += "&datetime_sanction__gt=" + this.date_retenues_from;
                path += " 00:00&datetime_sanction__lt=" + this.date_retenues_to + " 23:59";
                if (this.sanction_not_done) {
                    path += "&activate_not_done=true";
                }
            }
            let orderingFields = [];
            if (this.sortBySanction && this.tabIndex !== 0) orderingFields.push("sanction_decision__sanction_decision");
            if (this.sortByClasse && this.tabIndex !== 0) orderingFields.push("matricule__classe__year,matricule__classe__letter");
            orderingFields.push("matricule__last_name");
            path += `&ordering=${orderingFields.toString()}`;
            path += "&page_size=500";
            window.open(path);
        },
    },
    mounted: function () {
        this.show();
    },
};
</script>

